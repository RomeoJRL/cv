                        La 5G privée démarre doucement faute d'équipements dédiés
                        Selon un rapport publié hier par IDC, en raison des effets persistants de la pandémie de Covid-19 et du ralentissement dans le développement de terminaux équipés de la 5G, le rythme de croissance de la 5G/LTE privée dans l'entreprise devrait chuter considérablement par rapport aux estimations antérieures. En effet, si une prévision de décembre 2020 avançait que la taille du marché annuel des infrastructures sans fil privées LTE/5G atteindrait environ 4,7 milliards de dollars en 2023, le nouveau rapport revoit ce chiffre à la baisse, à environ 3,8 milliards de dollars. Par rapport au chiffre de 1,8 milliard de dollars de 2021, l'augmentation reste forte, mais la baisse est néanmoins substantielle.
                        
                        Toujours selon IDC, les tests de 5G privés continuent à augmenter et devraient déboucher demain sur un déploiement généralisé, mais les déploiements commerciaux de la technologie restent limités à « quelques clients grands comptes ». Selon Brandon Butler, directeur de recherche chez IDC et l'un des auteurs du rapport, à ce stade du déploiement des réseaux 5G, les prévisions antérieures tablaient sur l'existence d'un écosystème robuste de dispositifs d'extrémité 5G. Mais ce n'est pas le cas. La rareté des dispositifs industriels intégrés, des véhicules guidés automatisés et d'autres segments importants du secteur de l'IoT industriel pointée par le rapport, impacte particulièrement la croissance. « Nous pensions que la demande serait légèrement plus importante », a reconnu M. Butler.</p>
